#include <gtest.h>
#include "AP_Math.h"

struct testVector
{
    float input;
    float expected;
    testVector( float in, float exp ) : input(in), expected(exp) { }
};

//----------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------
//  FIXTURE:  Radian Tests
//
//----------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------
class RadianParamFixture : public ::testing::TestWithParam<testVector*>
{
};

TEST_P( RadianParamFixture, ExpectPass )
{
    ASSERT_EQ( radians( GetParam()->input ),
                        GetParam()->expected );
}

INSTANTIATE_TEST_CASE_P( AP_Math_Radian_BAT,
                         RadianParamFixture,
                         ::testing::Values( new testVector( 0.0, 0.0 ),
                                            new testVector( 1, 0.0174532925 ),
                                            new testVector( 90, 1.57079633 ),
                                            new testVector( -90, -1.57079633 ),
                                            new testVector( 360, 6.28318531 ),
                                            new testVector( -360, -6.28318531 ) ) );

